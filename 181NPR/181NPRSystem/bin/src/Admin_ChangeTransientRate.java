    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            if (familyRateChecker() && regularRateChecker()
                    && familyExtraBedRateChecker() && regularExtraBedRateChecker()
                    && additionalPrivateRateChecker() && privateSuiteRateChecker()) {
                if (JOptionPane.showConfirmDialog(null, "Are you sure you want to update the transient rates?", "Confirmation",
                        JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                    double[] rates = new double[6];
                    if (!familyRate.getText().trim().isEmpty()) {
                        rates[0] = Double.parseDouble(familyRate.getText());
                    } else {
                        rates[0] = 0.0;
                    }
                    if (!regularRate.getText().trim().isEmpty()) {
                        rates[1] = Double.parseDouble(regularRate.getText());
                    } else {
                        rates[1] = 0.0;
                    }
                    if (!familyExtraBedRate.getText().trim().isEmpty()) {
                        rates[2] = Double.parseDouble(familyExtraBedRate.getText());
                    } else {
                        rates[2] = 0.0;
                    }
                    if (!regularExtraBedRate.getText().trim().isEmpty()) {
                        rates[3] = Double.parseDouble(regularExtraBedRate.getText());
                    } else {
                        rates[3] = 0.0;
                    }
                    if (!privateSuiteRate.getText().trim().isEmpty()) {
                        rates[4] = Double.parseDouble(privateSuiteRate.getText());
                    } else {
                        rates[4] = 0.0;
                    }
                    if (!additionalPrivateRate.getText().trim().isEmpty()) {
                        rates[5] = Double.parseDouble(additionalPrivateRate.getText());
                    } else {
                        rates[5] = 0.0;
                    }
                    if (client.updateTransientRate(rates)) {
                        md.successful(this);
                        this.dispose();
                        new Administrator(client, user).setVisible(true);
                    } else {
                        md.unsuccessful(this);
                    }
                }
            } else {
                md.error(this, "Input Number must be greater than or equal to 0.");
            }
        } catch (RemoteException ex) {
//            Logger.getLogger(Admin_DefaultAccountsPaths.class.getName()).log(Level.SEVERE, null, ex);
            new MessageDialog().error(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            md.error(this, "Not a number");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

