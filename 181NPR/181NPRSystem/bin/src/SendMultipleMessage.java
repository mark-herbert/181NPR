    private void sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendActionPerformed
        // TODO add your handling code here:
        SwingUtilities.invokeLater(new Runnable() {

            @Override
            public void run() {
                send.setEnabled(false);
            }
        });
        try {
            int numberOfUnsentTexts = 0;
            boolean unsentTexts = false;
            for(int i = 0; i < model.getRowCount(); i++){
                ArrayList<String> desc = new ArrayList<>();
                desc.add(model.getValueAt(i, 2).toString());
                String type;
                String message = msg.getText();
                switch(template.getSelectedItem().toString()){
                    case "7th Day of Reservation":
                    case "11th Day of Reservation":
                        type = "Reservation";
                        break;
                    case "Successful Registration":
                        type = "Registration";
                        break;
                    case "Visitor":
                        type = "Visitor";
                        break;
                    case "Payment Due Date":
                        type = "Due";
                        break;
                    case "Balance":
                        type = "Balance";
                        break;
                    case "New Roommate":
                        type = "Roommate";
                        break;
                    case "Curfew":
                        message = message.replace("<name>", model.getValueAt(i, 1).toString());
                        type = "Curfew";
                        break;
                    default:
                        type = "others";
                }
                desc.add(message);
                desc.add(type);
                desc.add(model.getValueAt(i, 0).toString());
                desc.add(table);
                if(client.sendMessage(desc)){
                    progressBar.setValue(progressBar.getValue() + (100/model.getRowCount()));
                    progressBar.setString(progressBar.getValue()+"");
                    progressBar.update(progressBar.getGraphics());
                    try {
                        Thread.sleep(100);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(SendMultipleEmail.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    unsentTexts = true;
                    numberOfUnsentTexts++;
                }
            }
            new MessageDialog().sentMessages((JFrame) parent, model.getRowCount()-numberOfUnsentTexts, numberOfUnsentTexts);
        } catch (RemoteException ex) {
//            Logger.getLogger(SendMultipleEmail.class.getName()).log(Level.SEVERE, null, ex);
            new MessageDialog().error(null, ex.getMessage());
        }
        send.setEnabled(true);
    }//GEN-LAST:event_sendActionPerformed

    private void msgKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_msgKeyTyped
        // TODO add your handling code here:
        characters.setText("Characters: " + (159-msg.getText().length()));
        if(msg.getText().length() == 0){
            characters.setText("Characters: 160");
        }
        if(msg.getText().length() >= 160){
            characters.setText("Characters: 0");
        }
        if(msg.getText().length() == 159){
            mesg = msg.getText();
        }
        if(msg.getText().length() == 160){
            msg.setText(mesg);
        }
    }//GEN-LAST:event_msgKeyTyped

    private String updateRecipient(String idNumber){
        try {
            if(table.equals("resident")){
                return client.getNumber(idNumber).getResidentContact();
            } else {
                return client.getNumberTransientOrReservation(idNumber, table);
            }
        } catch (RemoteException ex) {
//            Logger.getLogger(SendMessage.class.getName()).log(Level.SEVERE, null, ex);
            new MessageDialog().error(null, ex.getMessage());
        }
        return "";
    }
    
    public String getReservationOrTransient(String name){
        try {
            if(table.equals("reservation")){
                return client.getReservationId(name);
            } else {
                return client.getTransientId(name);
            }
        } catch (RemoteException ex) {
//            Logger.getLogger(SendMultipleEmail.class.getName()).log(Level.SEVERE, null, ex);
            new MessageDialog().error(null, ex.getMessage());
        }
        return "";
    }
    
