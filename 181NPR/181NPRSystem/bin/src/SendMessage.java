    private void sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendActionPerformed
        SwingUtilities.invokeLater(new Runnable() {

            @Override
            public void run() {
                send.setEnabled(false);
            }
        });
        try {
            // TODO add your handling code here:
            desc.add(number.getText());
            desc.add(msg.getText());
            String type;
            switch(template.getSelectedItem().toString()){
                case "7th Day of Reservation":
                case "11th Day of Reservation":
                    type = "Reservation";
                    break;
                case "Successful Registration":
                    type = "Registration";
                    break;
                case "Visitor":
                    type = "Visitor";
                    break;
                case "Payment Due Date":
                    type = "Due";
                    break;
                case "Balance":
                    type = "Balance";
                    break;
                case "New Roommate":
                    type = "Roommate";
                    break;
                case "Curfew":
                    type = "Curfew";
                    break;
                default:
                    type = "others";
            }
            desc.add(type);
            desc.add(id.trim());
            desc.add("resident");
            if(client.sendMessage(desc)){
                new MessageDialog().message((JFrame) parent);
            } else {
                new MessageDialog().messageNotSent((JFrame) parent);
            }
        } catch (RemoteException ex) {
//            Logger.getLogger(SendMessage.class.getName()).log(Level.SEVERE, null, ex);
            new MessageDialog().error(null, ex.getMessage());
        }
        send.setEnabled(true);
    }//GEN-LAST:event_sendActionPerformed

    private void msgKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_msgKeyTyped
        // TODO add your handling code here:
        characters.setText("Characters: " + (159-msg.getText().length()));
        if(msg.getText().length() == 0){
            characters.setText("Characters: 160");
        }
        if(msg.getText().length() >= 160){
            characters.setText("Characters: 0");
        }
        if(msg.getText().length() == 159){
            mesg = msg.getText();
        }
        if(msg.getText().length() == 160){
            msg.setText(mesg);
        }
    }//GEN-LAST:event_msgKeyTyped

    private void templateItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_templateItemStateChanged
        // TODO add your handling code here:
        switch(template.getSelectedIndex()){
            case 0:
                tmpl = "";
                charCount = 0;
                break;
            case 1:
                tmpl = mt.getReservationSeventh();
                charCount = tmpl.length();
                break;
            case 2:
                tmpl = mt.getReservationThird();
                charCount = tmpl.length();
                break;
            case 3:
                tmpl = mt.getRegistration();
                charCount = tmpl.length();
                break;
            case 4:
                tmpl = mt.getVisitor();
                charCount = tmpl.length();
                break;
            case 5:
                tmpl = mt.getDueDate();
                charCount = tmpl.length();
                break;
            case 6:
                tmpl = mt.getBalance();
                charCount = tmpl.length();
                break;
            case 7:
                tmpl = mt.getNewRoommate();
                charCount = tmpl.length();
                break;
            case 8:
                if(isSecond){
                    openSendNotif();
                    isSecond = false;
                } else {
                    isSecond = true;
                }
                tmpl = mt.getCurfewViolation();
                tmpl = tmpl.replace("<residentName>", recipient.getText().trim());
                charCount = tmpl.length();
                break;
        }
        msg.setText(tmpl);
        characters.setText("Characters: " + (160 - charCount));
    }//GEN-LAST:event_templateItemStateChanged

