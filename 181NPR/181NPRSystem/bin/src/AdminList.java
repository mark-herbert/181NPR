    private void adminNamesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_adminNamesItemStateChanged
        try {
            if(iterate){
                AdminImpl adminImpl = client.getAdminProfile(adminNames.getSelectedItem().toString());
                lastName.setText(adminImpl.getLName());
                firstName.setText(adminImpl.getFName());
                middleName.setText(adminImpl.getMName());
                email.setText(adminImpl.getEmail());
                if (adminImpl.getPicture() != null) {
                    InputStream in = new ByteArrayInputStream(adminImpl.getPicture());
                    BufferedImage img = ImageIO.read(in);
                    if (img != null) {
                        Image image = img.getScaledInstance(picture.getWidth(), picture.getHeight(), Image.SCALE_SMOOTH);
                        picture.setIcon(new ImageIcon(image));
                    }
                }
                iterate = false;
            } else {
                iterate = true;
            }
        } catch (RemoteException ex) {
//            Logger.getLogger(AdminList.class.getName()).log(Level.SEVERE, null, ex);
            new MessageDialog().error(this, ex.getMessage());
        } catch (IOException ex) {
//            Logger.getLogger(AdminList.class.getName()).log(Level.SEVERE, null, ex);
            new MessageDialog().error(this, ex.getMessage());
        }
    }//GEN-LAST:event_adminNamesItemStateChanged

