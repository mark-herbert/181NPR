    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try {
            // TODO add your handling code here:
            if(md.confirmationSave(this) == md.YES){
                String d = new SimpleDateFormat("MMM dd, yyyy").format(birthdate.getDateFormat().parse(birthdate.getText()));
                adminImpl = new AdminImpl(last_name.getText(),first_name.getText(),middle_name.getText(),gender.getSelectedItem().toString(),d,email.getText(),null);
                if(file != null){
                    currentProf = Files.readAllBytes(new File(file).toPath());
                }
                adminImpl.setPicture(currentProf);
                adminImpl.setUser(user);
                if(!client.updateAdmin(adminImpl)){
                    md.successful(this);
                    this.dispose();
                    switch(origin){
                        case "main":
                            new Main(client, user).setVisible(true);
                            break;
                        case "iLogbook":
                            new ILogbook(client, user).setVisible(true);
                            break;
                    }
                } else {
                    md.unsuccessful(this, "Update Failed");
                }
            }
        } catch (RemoteException ex) {
//            Logger.getLogger(EditProfile.class.getName()).log(Level.SEVERE, null, ex);
            new MessageDialog().error(this, ex.getMessage());
        } catch (IOException | ParseException ex) {
//            Logger.getLogger(EditProfile.class.getName()).log(Level.SEVERE, null, ex);
            new MessageDialog().error(this, ex.getMessage());
        }
    }//GEN-LAST:event_saveActionPerformed

    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        // TODO add your handling code here:
        try {
            String path = "";
            BufferedReader br = new BufferedReader(new FileReader("dir\\docs.txt"));
            try {
                StringBuilder sb = new StringBuilder();
                String line = br.readLine();

                while (line != null) {
                    sb.append(line);
                    sb.append(System.lineSeparator());
                    line = br.readLine();
                }
                path = sb.toString().trim();
            } catch (IOException ex) {
//                Logger.getLogger(AdminArchiveFrame.class.getName()).log(Level.SEVERE, null, ex);
                new MessageDialog().error(this, ex.getMessage());
            } finally {
                br.close();
            }
            Desktop.getDesktop().browse(new File(path).toURI());
        } catch (IOException ex) {
            md.error(this, ex.getMessage());
//            Logger.getLogger(EditProfile.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jLabel1MouseClicked

