    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (JOptionPane.showConfirmDialog(null, "Are you sure you want to update the accounts and paths?", "Confirmation",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            try {
                accounts = new Accounts();
                accounts.setEmailAccount(email.getText().trim());
                accounts.setEmailPassword(emailPassword.getText().trim());
                accounts.setEmailPort(emailPort.getText().trim());
                accounts.setFowizPasscode(fowizPasscode.getText().trim());
                accounts.setFowizUsername(fowizUsername.getText().trim());
                accounts.setBiometrics(biometrics.getSelectedItem().toString());
                
                String time = hour.getValue() + ":" + minute.getValue() + ":" + second.getValue() + " " + a.getSelectedItem();
                time = new SimpleDateFormat("HH:mm:ss").format(new SimpleDateFormat("hh:mm:ss a").parse(time));
                
                accounts.setCurfew(time);
                
                if (client.updateAccounts(accounts)) {
                    md.successful(this);
                    this.dispose();
                    new Administrator(client,user).setVisible(true);
                } else {
                    md.unsuccessful(this);
                }
            } catch (RemoteException | ParseException ex) {
//                Logger.getLogger(Admin_DefaultAccountsPaths.class.getName()).log(Level.SEVERE, null, ex);
                new MessageDialog().error(this, ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

