    private boolean Null() {
        return !(dailyTextField.getText().length() != 0 || monthlyTextField.getText().length() != 0);
    }

    private boolean dailyTextFieldChecker() throws NumberFormatException {
        if (dailyTextField.getText().isEmpty() || Double.parseDouble(dailyTextField.getText()) >= 0) {
            return true;
        } else {
            return false;
        }
    }

    private boolean monthlyTextFieldChecker() throws NumberFormatException {
        if (monthlyTextField.getText().isEmpty() || Double.parseDouble(monthlyTextField.getText()) >= 0) {
            return true;
        } else {
            return false;
        }
    }

