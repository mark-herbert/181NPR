    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            if (monthlyTextFieldChecker() && dailyTextFieldChecker()) {
                if (md.confirmationSave(this) == md.YES) {
                    double[] rates = new double[2];
                    if (dailyTextField.getText().length() != 0) {
                        rates[0] = Double.parseDouble(dailyTextField.getText());
                    } else {
                        rates[0] = 0.0;
                    }

                    if (monthlyTextField.getText().length() != 0) {
                        rates[1] = Double.parseDouble(monthlyTextField.getText());
                    } else {
                        rates[1] = 0.0;
                    }

                    if (client.updateShuttleRate(rates)) {
                        md.successful(this);
                        setRates();
                    } else {
                        md.unsuccessful(this);
                    }
                }
            } else {
                md.error(this, "Input Number must be greater than or equal to 0.");
            }
        } catch (RemoteException ex) {
            md.error(this, ex.getMessage());
//            Logger.getLogger(Admin_ChangeShuttleRate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NumberFormatException ex) {
            md.error(this, "Not a number");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

