    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try {
            if (JOptionPane.showConfirmDialog(null, "Are you sure you want to change the room of " + name.getText() + "?", "Confirmation", JOptionPane.YES_NO_OPTION)
                    == JOptionPane.YES_OPTION) {
                if (client.updateBillingFromChangeRoom(residentId) && client.updateRoomStatus(roomNumber.getText(), roomNumberComboBox.getSelectedItem().toString(), residentId) &&
                        client.updateFurnitureToNull(residentId)) {
                    md.successful(this);
                    this.dispose();
                } else {
                    md.unsuccessful(this);
                }
            }
        } catch (RemoteException ex) {
//            Logger.getLogger(ChangeRoom.class.getName()).log(Level.SEVERE, null, ex);
            new MessageDialog().error(this, ex.getMessage());
        }
    }//GEN-LAST:event_saveActionPerformed

