    private void setLabels() {
        try {
            ArrayList<String> room1 = client.getResidentRoomDetail(residentId);
            name.setText(room1.get(0));
            roomNumber.setText(room1.get(1));
            roomType.setText(room1.get(2));
            dorm.setText(room1.get(3));
            ArrayList<String> room2 = client.getRooms();
            for(String r : room2){
                if(!r.equals(roomNumber.getText())){
                    roomNumberComboBox.addItem(r);
                }
            }
            selectedRoomDetails();
        }catch (RemoteException ex) {
//            Logger.getLogger(ChangeRoom.class.getName()).log(Level.SEVERE, null, ex);
            new MessageDialog().error(this, ex.getMessage());
        }
    }

    private void selectedRoomDetails() throws RemoteException {
        ArrayList<String> room3 = client.getRoomDetail(roomNumberComboBox.getSelectedItem().toString());
        roomTypeNew.setText(room3.get(0));
        dormNew.setText(room3.get(1));
    }
    
