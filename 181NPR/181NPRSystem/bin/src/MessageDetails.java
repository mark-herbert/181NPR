    private void displayList(){
        try {
            NotificationImpl notificationImpl = client.getMessageDetails(list);
            id.setText(notificationImpl.getNotifId());
            time.setText(list.get(0));
            name.setText(list.get(1));
            table.setText(list.get(4));
            date.setText(list.get(5));
            contact.setText(notificationImpl.getContactInfo());
            message.setText(notificationImpl.getMessage());
        } catch (RemoteException ex) {
//            Logger.getLogger(MessageDetails.class.getName()).log(Level.SEVERE, null, ex);
            new MessageDialog().error(null, ex.getMessage());
        }
    }
    
